
public class Casting {


	public static void main(String[] args) {
		byte smallValue = 6;
		int biggerDataType;
		biggerDataType = smallValue;
		System.out.println(biggerDataType);
		
		long bigAmount = 11111111111111l;
		byte smallVariable = (byte)bigAmount;
		System.out.println(smallVariable);
		
		
	}

}
